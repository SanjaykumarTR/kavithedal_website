// Helper function to get translation
export const getTranslation = (translationsObj, language, section, key, replacements = {}) => {
  const sectionTranslations = translationsObj[section]?.[language] || {};
  let text = sectionTranslations[key] || translationsObj.common[language][key] || key;
  
  // Replace placeholders like {count}, {title}, etc.
  Object.keys(replacements).forEach((replaceKey) => {
    text = text.replace(`{${replaceKey}}`, replacements[replaceKey]);
  });
  
  return text;
};
